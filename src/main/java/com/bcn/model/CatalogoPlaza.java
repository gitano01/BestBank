package com.bcn.model;

public class CatalogoPlaza {
	private int plazaId;
	private String codigoPlaza;
	private String poblacion;

	public int getPlazaId() {
		return plazaId;
	}

	public void setPlazaId(int plazaId) {
		this.plazaId = plazaId;
	}

	public String getCodigoPlaza() {
		return codigoPlaza;
	}

	public void setCodigoPlaza(String codigoPlaza) {
		this.codigoPlaza = codigoPlaza;
	}

	public String getPoblacion() {
		return poblacion;
	}

	public void setPoblacion(String poblacion) {
		this.poblacion = poblacion;
	}
}
